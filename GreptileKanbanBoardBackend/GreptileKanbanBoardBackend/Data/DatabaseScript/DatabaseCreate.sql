CREATE TABLE Columns (
    COLUMN_ID INT PRIMARY KEY,
    COLUMN_TITLE NVARCHAR(50)
);

CREATE TABLE Cards (
    ID INT PRIMARY KEY,
    TITLE NVARCHAR(100),
    COLUMN_ID INT,
    FOREIGN KEY (COLUMN_ID) REFERENCES Columns(COLUMN_ID)
);

